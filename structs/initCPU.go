package structs

type CPU struct {
	Registers [16]byte
	PC        uint16
	I         uint16
	SP        uint16
	DT        uint16
	ST        uint16
	Memory    [4096]byte
	Stack     [16]uint16
	Opcodes   []byte
	Screen    [64][32]byte
}

func (g *CPU) InitCPU(data []byte) {
	g.PC = 0x200
	g.InitMemory(data)
	g.AddOpcodesToCPU()
}

func (g *CPU) InitMemory(data []byte) {
	g.AddROMToMemory(data)
	g.AddFontSetToMemory()
}

func (g *CPU) ClearScreen() {
	g.Screen = [64][32]byte{}
}

func (g *CPU) AddROMToMemory(data []byte) {
	for i, b := range data {
		g.Memory[0x200+i] = b
	}
}

