package structs

type CPU struct {
	Registers []byte
	PC        byte
	I         byte
	SP        byte
	DT        byte
	ST        byte
	Memory    [4096]byte
	Stack     [32]byte
	Opcodes   []byte
	Screen    [32][64]int
}

func (c *CPU) InitCPU(data []byte) {
	c.InitMemory(data)
	c.AddOpcodesToCPU()
}

func (c *CPU) InitMemory(data []byte) {
	c.AddROMToMemory(data)
	c.AddFontSetToMemory()
}

func (c *CPU) ClearScreen() {
	c.Screen = [32][64]int{}
}

func (c *CPU) AddROMToMemory(data []byte) {
	for i, b := range data {
		c.Memory[0x200+i] = b
	}
}

